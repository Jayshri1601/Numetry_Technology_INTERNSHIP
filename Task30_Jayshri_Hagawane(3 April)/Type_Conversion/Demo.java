// Type Conversion

class Demo
{

	public static void main(String[] args)
	{
		int i = 50;

		// Integer to long type
		long l = i;

		// long to float type
		float f = l;

		System.out.println("Int value " + i);
		System.out.println("Long value " + l);
		System.out.println("Float value " + f);
	}
}
